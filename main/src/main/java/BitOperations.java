/**
 * Class with bit operations
 *
 * @author <a href="mailto:petr.ujezdsky@cleverlance.com">Petr Újezdský</a>
 * @version $Id$
 */
public class BitOperations {

    public long BITAND(long a, long b) {
        return a & b;
    }
    
    public long BITOR(long a, long b) {
        return a | b;
    }
}
